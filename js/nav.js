function Nav_SwitchOver(objTag, strState) {
	if (strState == 'Over') {
		objTag.bgColor='#CCCCCC';
	} else {
		objTag.bgColor='#FFFFFF';
	}
}

function Nav_SwitchOut(objTag, strState) {
	if (strState == 'Over') {
		objTag.bgColor='#CCCCCC';
	} else {
		objTag.bgColor='#FFFFFF';
	}
}

